import java.util.*;

class NumbersEquality{
	public static void main(String arg[]){
	System.out.print("Enter two numbers");
	Scanner sc=new Scanner(System.in);
	int a=sc.nextInt();
	int b=sc.nextInt();
	if(b==a)
	{System.out.println("Equal");}
	else if (b!=a)
	{System.out.print("Not Equal");}
	}


}